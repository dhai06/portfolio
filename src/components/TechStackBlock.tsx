interface TechStackBlockProps {
    onLike?: () => void;
}

export default function TechStackBlock({ onLike }: TechStackBlockProps) {
    const [liked, setLiked] = useState(false);

    const handleClick = () => {
        setLiked(!liked);
        onLike?.();
    };

    const techStack = [
        { name: 'React', icon: ReactIcon },
        { name: 'TypeScript', icon: TypeScriptIcon },
        { name: 'Python', icon: PythonIcon },
        { name: 'Node.js', icon: NodeIcon },
        { name: 'Next.js', icon: NextJsIcon },
        { name: 'Tailwind', icon: TailwindIcon },
    ];

    return (
        <div className="relative bg-white p-6 border border-[var(--border)] rounded-xl">
            <p className="text-sm text-[var(--foreground)]/60 mb-3">Skills</p>
            <div className="grid grid-cols-3 gap-4">
                {techStack.map((tech) => (
                    <motion.div
                        key={tech.name}
                        className="flex flex-col items-center gap-2 p-3 rounded-lg bg-[var(--background)] border border-[var(--border)]"
                        whileHover={{ scale: 1.05 }}
                        transition={{ duration: 0.2 }}
                    >
                        <tech.icon />
                        <span className="text-xs text-[var(--foreground)]/80">{tech.name}</span>
                    </motion.div>
                ))}
            </div>

            {/* Like button */}
            <button
                onClick={handleClick}
                className="absolute bottom-4 right-4 w-10 h-10 rounded-full bg-[var(--background)] shadow-md flex items-center justify-center border border-[var(--border)] hover:scale-105 transition-transform"
                aria-label="Like"
            >
                <motion.div
                    animate={{ scale: liked ? [1, 1.3, 1] : 1 }}
                    transition={{ duration: 0.3 }}
                >
                    <Heart
                        className={`w-5 h-5 ${liked ? 'fill-red-500 text-red-500' : 'text-[var(--foreground)]'}`}
                    />
                </motion.div>
            </button>
        </div>
    );
}
